


class ThreeTuple<A,B,C> {
	public  final A first;
	public final B second;
	public final C third;
	public ThreeTuple(A a,B b,C c){
		first=a;
		second=b;
		third=c;
	}
	
	public A getFirst(){
		return first; 
	}
	
	public B getSecond(){
		return second; 
	}
	public C getThird(){
		return third; 
	}

	

}
